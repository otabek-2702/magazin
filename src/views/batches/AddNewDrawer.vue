<script setup>
import { PerfectScrollbar } from 'vue3-perfect-scrollbar';
import { nextTick, ref } from 'vue';
import AppDrawerHeaderSection from '@core/components/AppDrawerHeaderSection.vue';
import axios from '@axios';
import { toast } from 'vue3-toastify';

const props = defineProps({
  isDrawerOpen: {
    type: Boolean,
    required: true,
  },
});

const emit = defineEmits(['update:isDrawerOpen', 'fetchDatas']);

const isFetching = ref(false);
const isFormValid = ref(false);
const refForm = ref();
const name = ref();
const description = ref();
const road_expenses = ref();

// üëâ drawer close
const closeNavigationDrawer = () => {
  emit('update:isDrawerOpen', false);
  nextTick(() => {
    refForm.value?.reset();
    refForm.value?.resetValidation();
  });
};

const onSubmit = () => {
  refForm.value?.validate().then(async ({ valid }) => {
    if (valid) {
      isFetching.value = true;
      try {
        await axios.post('/batches', {
          name: name.value,
          road_expenses: road_expenses.value,
          description: description.value,
        });
        emit('fetchDatas');
        toast('–£—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω–æ', {
          theme: 'auto',
          type: 'success',
          dangerouslyHTMLString: true,
        });
        handleDrawerModelValueUpdate(false);
      } catch (error) {
        console.error(error);
      } finally {
        isFetching.value = false;
      }
    }
  });
};

const handleDrawerModelValueUpdate = (val) => {
  emit('update:isDrawerOpen', val);
  if (!val) {
    nextTick(() => {
      refForm.value?.reset();
      refForm.value?.resetValidation();
    });
  }
};
</script>

<template>
  <VNavigationDrawer
    temporary
    :width="400"
    location="end"
    class="scrollable-content"
    :model-value="props.isDrawerOpen"
    @update:model-value="handleDrawerModelValueUpdate"
  >
    <!-- üëâ –ó–∞–≥–æ–ª–æ–≤–æ–∫ -->
    <AppDrawerHeaderSection title="–î–æ–±–∞–≤–∏—Ç—å" @cancel="closeNavigationDrawer" />

    <PerfectScrollbar :options="{ wheelPropagation: false }">
      <VCard flat>
        <VCardText>
          <!-- üëâ –§–æ—Ä–º–∞ -->
          <VForm ref="refForm" v-model="isFormValid" @submit.prevent="onSubmit">
            <VRow>
              <!-- üëâ –ü–æ–ª–Ω–æ–µ –∏–º—è -->
              <VCol cols="12">
                <VTextField v-model="name" label="–ò–º—è" autofocus />
              </VCol>

              <VCol cols="12">
                <VTextField v-model="road_expenses" label="–î–æ—Ä–æ–∂–Ω—ã–µ —Ä–∞—Å—Ö–æ–¥—ã" type="number" />
              </VCol>
              <VCol cols="12">
                <VTextarea label="–û–ø–∏—Å–∞–Ω–∏–µ" v-model="description" />
              </VCol>

              <!-- üëâ –û—Ç–ø—Ä–∞–≤–∏—Ç—å –∏ –û—Ç–º–µ–Ω–∞ -->
              <VCol cols="12">
                <VBtn :loading="isFetching" :disabled="isFetching" type="submit" class="me-3">
                  –û—Ç–ø—Ä–∞–≤–∏—Ç—å
                </VBtn>
                <VBtn type="reset" variant="tonal" color="secondary" @click="closeNavigationDrawer">
                  –û—Ç–º–µ–Ω–∞
                </VBtn>
              </VCol>
            </VRow>
          </VForm>
        </VCardText>
      </VCard>
    </PerfectScrollbar>
  </VNavigationDrawer>
</template>
