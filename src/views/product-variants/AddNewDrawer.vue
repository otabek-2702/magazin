<script setup>
import { PerfectScrollbar } from "vue3-perfect-scrollbar";
import { nextTick, onMounted, ref, watchEffect } from "vue";
import AppDrawerHeaderSection from "@core/components/AppDrawerHeaderSection.vue";
import axios from "@axios";
import { toast } from "vue3-toastify";

const props = defineProps({
  isDrawerOpen: {
    type: Boolean,
    required: true,
  },
});

const emit = defineEmits(["update:isDrawerOpen", "fetchDatas"]);

const isFetching = ref(false);
const isFormValid = ref(false);
const refForm = ref();
const product_id = ref();
const sizes_id = ref();
const colors_id = ref();
const sale = ref(0);

const onSubmit = () => {
  refForm.value?.validate().then(async ({ valid }) => {
    if (valid) {
      isFetching.value = true;
      try {
        await axios.post("/product_variants", {
          product_id: product_id.value,
          sizes_list: sizes_id.value,
          colors_list: colors_id.value,
          sale: sale.value,
        });
        emit("fetchDatas");
        toast("–£—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω–æ", {
          theme: "auto",
          type: "success",
          dangerouslyHTMLString: true,
        });
        handleDrawerModelValueUpdate(false);
      } catch (error) {
        console.error(error);
      } finally {
        isFetching.value = false;
      }
    }
  });
};

const handleDrawerModelValueUpdate = (val) => {
  emit("update:isDrawerOpen", val);
  if (!val) {
    nextTick(() => {
      refForm.value?.reset();
      refForm.value?.resetValidation();
    });
  }
};

const products_list = ref([]);
const sizes_list = ref([]);
const colors_list = ref([]);

const fetchOptions = async (
  url,
  dataState,
  key,
  customization = { is: false }
) => {
  try {
    const response = await axios.get(url);

    if (response.status === 200) {
      if (customization.is) {
        dataState.value = response.data[key].map(customization.method);
      } else {
        dataState.value = response.data[key];
      }
    }
  } catch (error) {
    console.log(error);
  }
};

watch(
  () => props.isDrawerOpen,
  () => {
    fetchOptions("/products", products_list, "products");
    fetchOptions("/sizes", sizes_list, "sizes");
    fetchOptions("/colors", colors_list, "colors");
  },
  { once: true }
);
</script>

<template>
  <VNavigationDrawer
    temporary
    :width="400"
    location="end"
    class="scrollable-content"
    :model-value="props.isDrawerOpen"
    @update:model-value="handleDrawerModelValueUpdate"
  >
    <!-- üëâ –ó–∞–≥–æ–ª–æ–≤–æ–∫ -->
    <AppDrawerHeaderSection
      title="–î–æ–±–∞–≤–∏—Ç—å"
      @cancel="handleDrawerModelValueUpdate(false)"
    />

    <PerfectScrollbar :options="{ wheelPropagation: false }">
      <VCard flat>
        <VCardText>
          <!-- üëâ –§–æ—Ä–º–∞ -->
          <VForm ref="refForm" v-model="isFormValid" @submit.prevent="onSubmit">
            <VRow>
              <VCol cols="12">
                <VSelect
                  v-model="product_id"
                  label="–í—ã–±–µ—Ä–∏—Ç–µ —Ç–æ–≤–∞—Ä"
                  :items="products_list"
                  item-title="name"
                  item-value="id"
                  autofocus
                />
              </VCol>

              <VCol cols="12">
                <VSelect
                  v-model="sizes_id"
                  label="–í—ã–±–µ—Ä–∏—Ç–µ —Ä–∞–∑–º–µ—Ä"
                  :items="sizes_list"
                  item-title="name"
                  item-value="id"
                  multiple
                />
              </VCol>

              <VCol cols="12">
                <VSelect
                  v-model="colors_id"
                  label="–í—ã–±–µ—Ä–∏—Ç–µ —Ü–≤–µ—Ç"
                  :items="colors_list"
                  item-title="name"
                  item-value="id"
                  multiple
                />
              </VCol>

              <VCol cols="12">
                <VTextField
                  v-model="sale"
                  label="–°–∫–∏–¥–∫–∞ –≤ –ø—Ä–æ—Ü–µ–Ω—Ç–∞—Ö"
                  type="number"
                />
              </VCol>

              <!-- üëâ –û—Ç–ø—Ä–∞–≤–∏—Ç—å –∏ –û—Ç–º–µ–Ω–∞ -->
              <VCol cols="12">
                <VBtn
                  :loading="isFetching"
                  :disabled="isFetching"
                  type="submit"
                  class="me-3"
                >
                  –û—Ç–ø—Ä–∞–≤–∏—Ç—å
                </VBtn>
                <VBtn
                  type="reset"
                  variant="tonal"
                  color="secondary"
                  @click="handleDrawerModelValueUpdate(false)"
                >
                  –û—Ç–º–µ–Ω–∞
                </VBtn>
              </VCol>
            </VRow>
          </VForm>
        </VCardText>
      </VCard>
    </PerfectScrollbar>
  </VNavigationDrawer>
</template>
